def get_hint(code: str, name: str) -> str:
    """특기코드 또는 특기명으로 힌트 반환. 없으면 빈 문자열."""
    if code in CODE_HINTS:
        return CODE_HINTS[code]
    for keyword, hint in NAME_HINTS.items():
        if keyword in name:
            return hint
    return ""
